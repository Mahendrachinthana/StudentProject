package Modal;

public class CourceModal {
	String CourseName;
	String CoursedDuration;
    int	CourseFee;
	public String getCourseName() {
		return CourseName;
	}
	public void setCourseName(String courseName) {
		CourseName = courseName;
	}
	public String getCoursedDuration() {
		return CoursedDuration;
	}
	public void setCoursedDuration(String coursedDuration) {
		CoursedDuration = coursedDuration;
	}
	public int getCourseFee() {
		return CourseFee;
	}
	public void setCourseFee(int courseFee) {
		CourseFee = courseFee;
	}
}