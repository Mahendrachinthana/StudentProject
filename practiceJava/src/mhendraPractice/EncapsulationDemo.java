package mhendraPractice;

public class EncapsulationDemo {
	public  int empi;
protected	String empn;
	public  double emps;
	

    public int  getempi() {
    	return empi;	
    	}
    public void setempi(int empi) {
    	this.empi=empi;
    }
    public String getempn() {
    	return empn;

    }
    public void setempn(String empn ) {
    	this.empn=empn;
    }
    public double getemps() {
    	return emps;
    	
    }
	public void setemps(double emps) {
		this.emps=emps;
	}
}
